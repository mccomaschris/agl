			<h3 class="font-semibold mb-2 lg:text-xl mt-6" id="teams">Teams</h3>
			<p class="mb-3">There are six (6) teams composed of 4 players each.</p>
			<p class="mb-3">At the beginning of each year, players are grouped into one of four groups, "One" players
				through "Four" players. Returning players handicap is based on their handicap at the end of the previous
				season (not including playoff rounds). The six players with the lowest handicap will be the #1 players,
				the next six will be the #2 players, etc. In the event of a tie in handicap to determine the rank of a
				player, the lowest handicap to four decimal places will be used to determine the ranking. For example,
				if there are two players that could be #3 players and one player has a handicap of 9.33 and another has
				9.35, the 9.33 player will be the #3 player and the 9.35 will be a #4 player. If a tie persists to two
				decimal points (both players are 9.33), the #3 player will be determined by calculating the lowest HC
				based on the average of ALL regular season scores of the previous year's matches. If this does not
				resolve the tie, the #3 player will be drawn out of a hat.</p>
			<p class="mb-3">Each team captain (the #1 player) will be assigned a team number. The #2 players will be
				drawn at random. The first #2 player will be assigned to Team 1, the second #2 player drawn will be
				assigned to Team 2, and so on. This will also be done for the #3 and #4 players.</p>
			<p class="mb-3">A schedule of matches will be provided to each player at the beginning of the season. Each
				player will eventually be paired w/ every other player in the league. The first 5 weeks will have the #1
				and #2 players paired together and the #3 and #4 paired together. For the 5-10th weeks, the #1 and #3
				are paired as are #2 and #4. For weeks 10-15, players #1 and #4 will be paired with #2 and #3 paired.
				For the last 5 weeks we will revert to the same pairings as the first five weeks.</p>

