			<h3 class="font-semibold mb-2 lg:text-xl mt-6" id="handicaps">Handicaps</h3>
			<p class="mb-3">A player's league handicap is figured as the difference between par (currently 37 on the
				front nine at Riviera) and the player's average score. For example, if a player's average score is 40,
				their handicap would be 3 (40-37=3). As league play continues, a handicap may be a fraction. Fractions
				greater than or equal to .5 are rounded up, less than .5 are rounded down. So, a handicap of 4.55 would
				become 5, while a handicap of 4.48 would become 4.</p>
			<p class="mb-3">To start the new season returning players' handicaps will be based on their year-end
				handicap which is the best 50% or better of the scores from the prior season. New players joining at the
				beginning of the season will be the average of the scores that person has on the front 9 of Riviera or
				an honest assessment of his likely average score on the front 9 of Riviera.</p>
			<p class="mb-3">In season handicaps are recalculated every five weeks using the best 50% or better of each
				player's scores. For example, if a player plays in all 10 matches the first two quarters of the season
				their handicap will be based on the average of their 5 best scores. If a player only plays in 9 matches
				in their first two quarters of the season their handicap will still be based on the average of their 5
				best scores.</p>
			<p class="mb-3">The ranking of players (i.e., 1-4 players) will be based on the year-end handicap which is
				the best 50% or better of the scores.</p>
			<p class="mb-3">Assuming that a player plays all 20 weeks, the HC will be figured as follows:</p>
			<ul class="mb-4 list-inside list-disc pl-6">
				<li>For the 6th week, the best 3 scores of the previous 5 weeks will be used</li>
				<li>For the 11th week, the best 5 scores of the previous 10 weeks will be used</li>
				<li>For the 16th week, the best 8 scores of the previous 15 weeks will be used</li>
			</ul>
			<p class="mb-3">For the Team Playoffs and Individual League Championship, the best 10 scores of the entire
				season will be used. This HC will be used for any/all of the team playoffs (9-hole match-play) and the
				individual League Championship Tournament (18-holes stroke-play).</p>
			<p class="mb-3">HC for the League Championship at the end of the season will be 1.5 times the 9-hole HC. For
				example, a player w/ a HC of 4 will have a HC of 6 for the 18-hole tournament. A player w/ a HC of 7
				will have a HC of 11 (7+3.5= 10.5 rounded to 11).</p>
			<p class="mb-3">We do not compute handicap based on USGA rules.</p>

			<h4 class="mb-2" id="sub-handicap">Substitute Handicap</h4>
			<p class="mb-3">Handicaps for substitutes entering league play in mid-year will be treated exactly as new
				players starting at the beginning of the year. THe starting handicap will be initially based on as
				honest an assessment of his rounds at Riviera that can be made. Once he begins league play, his handicap
				will be based ONLY on the rounds played in LEAGUE PLAY. The handicap will then change as per league
				rules. His handicap will be changed every 5 weeks as is league policy. If, for instance, he joins the
				league in week 8, his handicap will be the same for Week 8, 9, and 10 then will change with the rest of
				the league for Week 11.</p>

			<h3 class="font-semibold mb-2 lg:text-xl mt-6" id="schedule">Schedule and Cancellations</h3>
			<p class="mb-3">There are 20 regularly scheduled matches each season. In the event of rain, a maximum of
				three make-up weeks will be played at the end of the regular season. Team Championships (9-holes, match
				play) will be played the weeks immediately following regular season play. The Individual League
				Championship (18-holes played on a Sunday) is usually scheduled in mid-September.</p>
			<p class="mb-3">In the case of a rain-out or cancellation, the cancelled match will be played the following
				week. In effect, the schedule gets pushed back one week. If rain begins after play begins, the match
				will be counted as complete only if all groups have completed at least 5 holes. If 5 holes are not
				completed by all groups, the match is not counted. If a match is completed after 5 holes, the scoring
				and handicap calculation will be based on a player's current handicap.</p>
			<p class="mb-3">In the event of rain before a match begins, it is each players responsibility to be at the
				course and ready to play. If, however, you have been called and informed that the match has been
				cancelled or you have called the course and found that the course is closed, you need not show up. If
				the course is open, we WILL meet at the course to determine if we will play. If you choose not to come
				out or not to play, you will be counted as absent.</p>
			<p class="mb-3">Tee times generally begin at 5:00. We will tee off in order listed on your scorecard. If all of a group is not there, they will be skipped and put at the end of the line.</p>
			<p class="mb-3">If we cannot tee off at our designated time (due to weather, etc.) we can have a "shotgun"
				start w/ groups beginning on #1, #3, and #5.</p>

			<h3 class="font-semibold mb-2 lg:text-xl mt-6" id="rules-play">Rules Governing Play</h3>
			<p class="mb-3">Play in the Appliance League is Match Play. You can read more about <a
					href="http://www.usga.org/rules/rules-and-decisions.html#!rule-02">Match Play on the USGA
					website</a>.</p>
			<p class="mb-3">BE COURTEOUS TO YOUR FELLOW COMPETITORS. AVOID SLOW PLAY AND BE READY TO TEE OFF AT YOUR
				ASSIGNED TEE TIME. No one likes to have their tee time delayed or be held up while out on the course.
			</p>
			<p class="mb-3">If you hit a ball other than your own, you lose the hole and are assessed a two-stroke
				penalty for the hole.</p>
			<p class="mb-3">The maximum allowed score for each hole is two more strokes than your opponent on that hole.
			</p>
			<p class="mb-3">The ball can be played up and can be moved one hand's width (tip of little finger to tip of
				thumb, both extended) anywhere except the green to improve the lie. We want to have fun playing this
				gentleman's game.</p>
			<p class="mb-3">In that Riviera has a history of not manicuring the bunkers players may lift the ball in the bunker and
				rake the area where the ball came to rest raked. Shots <strong>MUST</strong> be played from the bunker.</p>
			<p class="mb-3">Number four players must declare their intention to play white or yellow tees prior to the
				annual Snowball Tournament. The player will be required to play the ENTIRE season and playoffs from the
				same set of tees.</p>
			<p class="mb-3">Any players aged 75 and older, regardless of handicap, will be eligible to play the senior
				tees.</p>
			<p class="mb-3">In the event of a tie for the weekly game (low net or closest to the pin), the tie will be broken by the tied player's scoring on individual holes, using handicap, starting on hole 9 and working backward until a tie is broken.</p>

			<h3 class="font-semibold mb-2 lg:text-xl mt-6" id="attendance">Attendance</h3>
			<p class="mb-3">Any missed matches must be made up within the current quarter of the schedule. All matches
				must be made up and scorecards <a href="mailto:mccomas.chris@gmail.com">sent to the webmaster</a> by
				Wednesday evening prior to the next quarter starting. If you miss Week 8, you have until the night
				before Week 11's Thursday play to make-up your match and send your scorecard to the website to ensure
				handicaps are updated and ready for Week 11.</p>
			<p class="mb-3">Opposing players are obligated to make every effort to accommodate the player who is missing
				the match. The proper handicap will be applied to the match that would be applied should the match be
				played at the scheduled date. If you pre-play a Thursday match and that regular Thursday match is rained
				out, your pre-play match is also considered rained out and is null and void.</p>
			<p class="mb-3">If during this time a player misses five matches he will be replaced immediately.</p>
			<p class="mb-3">In the event of medical issue, serious injury or illness, either to the player or a family
				member that needs their care, accommodations will be made to replace that player with a temporary player
				while his absence is required. Once the player is able to return to regular play, the temporary player
				will be released and the member will rejoin the League play.</p>
			<p class="mb-3">If a member of your foursome is not present at your appointed tee time, the next foursome
				will be allowed to tee off ahead of your group. Your foursome continues to get bumped back until your
				entire foursome is present. Once all of your group is present, you can tee off. If you know that you
				will not be able to play on a particular week, <strong>CALL OR TEXT YOUR OPPONENT</strong> and let them
				know. This saves us all time and trouble. Likewise, if you know that one of your group will not be
				present, your threesome can tee off at its appropriate time, assuming that all of you are there and
				ready to play.</p>

			<h3 class="font-semibold mb-2 lg:text-xl mt-6" id="scoring">Scoring</h3>
			<p class="mb-3">Points are awarded as follows:</p>
			<ul class="mb-4 list-inside list-disc pl-6">
				<li>2 points for the winner of a match</li>
				<li>1 point for a tie</li>
				<li>0 points for a loss</li>
			</ul>
			<p class="mb-3">If a player is absent, his opponent automatically gets 1 point for showing up and playing
				and can win a 2nd point by beating or tying his partner's opponent. If neither opponent of one team
				shows up, the opponents that did show up receive two points each, but they must complete their round in
				order to receive the points. For example, if the number 1 and 2 players of Team 1 do not show up and
				their opponents on Team 2 do show up and play, both of the Team 2 players get 2 points. The same applies
				if all members of a team do not show up.</p>

			<h3 class="font-semibold mb-2 lg:text-xl mt-6" id="team-championship">Team Championship</h3>
			<p class="mb-3">Each team accumulates points based on matches won, lost or tied. To determine the Team
				Champion for the year, a playoff system has been devised. In the first round, the team finishing the
				season in 1st place will play the team finishing the season in 4th place and the team finishing the
				season in 2nd place will play the team finishing in 3rd place. The two winners will then play the
				championship match. The Team playoffs will begin the week immediately following regular season play.</p>
			<p class="mb-3">Format for the Team Championships will be same as regular season play, match play.</p>
			<p class="mb-3">Teams are ranked in the standings by Total Points, then #1 Player Points, then #2 Player
				Points, then #3 Player Points, and finally #4 Player Points.</p>
			<p class="mb-3">In the event of a tie, the winner will be determined by comparing the score of the two
				closest handicap opponents starting with the #1 handicap hole (and up, as needed) to determine who had
				the low net score on that hole. For example:
			<ul class="mb-4 list-inside list-disc pl-6">
				<li>One Players: 2 HC vs 3 HC</li>
				<li>Two Players: 4 HC vs 6 HC</li>
				<li>Three Players: 9 HC vs 7 HC</li>
				<li>Four Players: 11 HC vs 11 HC</li>
			</ul>
			In this scenario you would compare the score of the four players starting with the #1 handicap hole (and up,
			as needed) to determine who had the low net score on that hole.
			</p>
			<p class="mb-3">There are no substitutes allowed for playoff matches.</p>

			<h3 class="font-semibold mb-2 lg:text-xl mt-6" id="individual-championship">Individual Championship</h3>
